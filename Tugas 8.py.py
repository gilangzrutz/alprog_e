x = 24
y = 66

# Operasi Penjumlahan
hasil = x + y
print("nilai dari x + y = ", hasil)

# Operasi Pengurangan
hasil = x - y
print("hasil dari x - y = ", hasil)

# Operasi Pembagian 
hasil = x / y
print("hasil dari x / y = ", hasil)

# Operasi Perkalian
hasil = x * y
print("hasil dari x * y =", hasil)

# Operasi Exsponen
hasil = x ** y
print("hasil dari x ** y = ", hasil)

# Operasi Modulus
hasil = x % y
print("hasil dari x % y = ", hasil)

# Operasi Floor Division
hasil = x // y
print("hasil dari x // y = ", hasil)